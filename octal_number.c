#include <stdio.h>

int main()
{
    int octalNumber = 0257;  // Oktal (Base-8) Only 0-7 numbers are used
    int decimalNumber = 175; // Decimal (Base-10)

    printf("Octal: %o\n", octalNumber);   // %o octal format
    printf("Decimal: %d\n", octalNumber); // It is written in decimals

    return 0;
}
